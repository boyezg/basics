// if statement have only one condition that is true condition only

var a = 10;
if(a>0){
    console.log("a is positive");
}


// if else statement have two condition that is true and false condition    

var a = 10;
if(a>0){
    console.log("a is positive");
}
else{
    console.log("a is negative");
}


// if else if statement have multiple condition that is true and false condition

var a = 10;
if(a<5){
    console.log("a is less than 5");
}
else if(a>5){
    console.log("a is greater than 5");
}
else{
    console.log("a is equal to 5");
}   